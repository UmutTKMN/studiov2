CREATE TABLE `users` (
  `user_id` int(11) NOT NULL,
  `user_name` varchar(100) NOT NULL,
  `user_email` varchar(150) NOT NULL,
  `user_password` varchar(255) NOT NULL,
  `user_bio` text DEFAULT NULL,
  `user_role` int(11) NOT NULL,
  `user_profileImage` varchar(255) DEFAULT NULL,
  `user_university` varchar(150) DEFAULT NULL,
  `user_department` varchar(150) DEFAULT NULL,
  `user_graduationYear` int(11) DEFAULT NULL,
  `user_degree` varchar(50) DEFAULT NULL,
  `user_company` varchar(150) DEFAULT NULL,
  `user_position` varchar(100) DEFAULT NULL,
  `user_yearsOfExperience` int(11) DEFAULT NULL,
  `user_currentlyWorking` tinyint(1) DEFAULT 0,
  `user_instagram` varchar(255) DEFAULT NULL,
  `user_twitter` varchar(255) DEFAULT NULL,
  `user_linkedin` varchar(255) DEFAULT NULL,
  `user_github` varchar(255) DEFAULT NULL,
  `user_country` varchar(100) DEFAULT NULL,
  `user_city` varchar(100) DEFAULT NULL,
  `user_postalCode` varchar(20) DEFAULT NULL,
  `user_theme` enum('light','dark') DEFAULT 'light',
  `user_language` varchar(10) DEFAULT 'en',
  `user_emailNotifications` tinyint(1) DEFAULT 1,
  `user_pushNotifications` tinyint(1) DEFAULT 1,
  `user_isActive` tinyint(1) DEFAULT 1,
  `user_isVerified` tinyint(1) DEFAULT 0,
  `user_last_login` datetime DEFAULT current_timestamp(),
  `user_createdAt` datetime DEFAULT current_timestamp(),
  `user_login_count` int(11) DEFAULT 0,
  `user_failed_login_attempts` int(11) DEFAULT 0,
  `user_last_failed_login` datetime DEFAULT NULL
  `login_count` int(11) DEFAULT 0,
  `failed_login_attempts` int(11) DEFAULT 0,
  `last_failed_login` datetime DEFAULT NULL
)
